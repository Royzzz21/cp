[제 6조 개인정보의 기술적·관리적 보호대책]<br />
<br />
우리는 귀하의 데이터가 본 개인 정보 보호 정책에 따라 안전하게 취급되도록 합리적으로 필요한 모든 조치를 취할 것입니다.<br />
1. 기술적 대책<br />
<br />
① 데이터의 암호화<br />
<br />
귀하가 제공 한 모든 정보는 당사의 보안 서버에 저장됩니다. 모든 웹 기반 지불 거래는 SSL 기술을 사용하여 암호화됩니다.<br />
<br />
② 해킹 등에 대비한 대책<br />
<br />
웹사이트는 해킹이나 컴퓨터 바이러스 등에 의해 귀하의 개인정보가 유출되거나 훼손되는 것을 막기 위해 최선을 다하고 있습니다. 개인정보의 훼손에 대비해서 자료를 수시로 백업하고, 백신프로그램을 이용하여 이용자들의 개인정보나 자료가 누출되거나 손상되지 않도록 방지합니다. 그리고 침입차단시스템을 이용하여 외부로부터의 무단 접근을 통제하며, 기타 시스템적으로 보안성을 확보하기 위한 가능한 모든 기술적 장치를 갖추려 노력하고 있습니다.<br />
<br />
2. 관리적 대책<br />
<br />
① 취급 직원의 최소화 및 교육 웹사이트의 개인정보관련 취급 직원은 담당자에 한정시키고, 이를 위한 별도의 비밀번호를 부여하여 정기적으로 갱신하며, 담당자에 대한 수시 교육을 통하여 웹사이트의 개인정보 취급방침의 준수를 항상 강조합니다.<br />
<br />
3. 제공 받은 개인 정보의 관리<br />
<br />
이용자의 개인적인 부주의로 비밀번호, 핸드폰번호 등 개인정보가 유출되어 발생한 문제와 기본적인 인터넷의 위험성 때문에 일어나는 일들에 대해 책임을 지지 않습니다. 보안 의식을 가지고 비밀번호를 자주 변경하며 공용PC에서의 사용 시 개인정보가 유출되지 않도록 각별한 주의를 기울여 주시기 바랍니다.<br />
