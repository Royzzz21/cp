[TIBAB 개인정보 정책]<br />
<br />
본 이용 약관(이하 '약관')은 TIBAB 웹사이트 접근한 귀하로부터 수집하거나 웹사이트에 제공한 개인 정보가 웹사이트에 의해 처리 되는 근거를 제시합니다. 웹사이트에 계속 접근하거나 사용함으로써 귀하는 본 약관에 구속되는 데 동의하게 됩니다. 본 약관의 모든 조건에 동의하지 않으면 웹사이트를 사용하거나 접속해서는 안됩니다.<br />
<br />
<br />
[제 1조 총칙]<br />
<br />
1. 본 웹사이트는 귀하의 개인정보보호를 매우 중요시하며, 『정보통신망이용촉진등에관한법률』상의 개인정보보호 규정 및 정보통신부가 제정한 『개인정보보호지침』을 준수하고 있습니다.<br />
2. 본 웹사이트는 개인정보보호방침을 통하여 귀하께서 제공하시는 개인정보가 어떠한 용도와 방식으로 이용되고 있으며 개인정보보호를 위해 어떠한 조치가 취해지고 있는지 알려드립니다.<br />
3. 본 웹사이트는 개인정보보호방침을 홈페이지 첫 화면 하단에 공개함으로써 귀하께서 언제나 용이하게 보실 수 있도록 조치하고 있습니다.<br />
4. 본 웹사이트는 개인정보취급방침을 개정하는 경우 웹사이트 공지사항(또는 개별공지)을 통하여 공지할 것입니다.<br />
