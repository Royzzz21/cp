[제 3조 정보의 수집 및 이용 목적]<br />
<br />
1. 귀하가 웹사이트에 제공하는 정보는 귀하가 당사 사이트 TIBAB.io를 통해서 기입한 정보 혹은 전화로 제공 한 개인의 정보를 말합니다. 여기에는 사이트를 사용하기 위해 등록하거나, 토론 게시판이나 기타 소셜미디어 기능에 참여할 때 당사에 제공 한 정보가 포함됩니다. 귀하가 웹사이트에서 문제를 보고 할 때, 귀하가 제공 한 정보(이름, 주소, 전자 메일 주소, 전화 번호, 웹사이트 URL, 신용 카드 정보를 포함 한 개인 정보)가 포함 될 수 있습니다.<br />
2. 귀하가 본 사이트를 방문 할 때마다 다음 정보를 자동으로 수집합니다.<br />
: 컴퓨터를 인터넷에 연결하는 데 사용되는 인터넷 프로토콜 (IP) 주소, 브라우저 유형 및 버전, 시간대 설정, 브라우저 플러그 인 유형 및 버전, 운영 체제 및 플랫폼을 포함한 기술 정보, 전체 URL (Uniform Resource Locator), 다운로드 오류, 특정 페이지 방문 기간 등 귀하의 방문에 대한 정보<br />
